    public void setNombre(String id){
        this.id=id;
        txtID.setText(id);
        try {
            conecta = new ConexionBD();
            String sql="SELECT l.titulo, g.descripcion FROM libros l INNER JOIN generos g ON l.id_genero=g.id_genero WHERE l.id_libro="+Integer.parseInt(id);
            res = conecta.consultar(sql);
            while(res.next()){
                txtTitulo.setText(res.getString("titulo"));
                cmbGeneros.setSelectedItem(res.getString("descripcion"));
            }
        } 
        catch (Exception e) {
        }
    }
    
