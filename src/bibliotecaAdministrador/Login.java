    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:
        System.out.println(txtPassword.getText());
        if(!txtUser.getText().equals("") && !txtPassword.getText().equals("")){
            conecta = new ConexionBD();
            ResultSet res;
            //String sqlConsulta="SELECT u.id_usuario, CONCAT(u.nombre,' ',u.ap,' ',u.am) as nombre, u.contrasena, t.descripcion FROM usuarios u INNER JOIN tipos t ON u.id_tipo = t.id_tipo where CONCAT(u.nombre,' ',u.ap,' ',u.am)='"+txtUser.getText()+"' and contrasena='"+txtPassword.getText()+"'";
            String sqlConsulta="SELECT u.id_usuario, CONCAT(u.nombre,' ',u.ap,' ',u.am) as nombre, u.contrasena, t.descripcion FROM usuarios u INNER JOIN tipos t ON u.id_tipo = t.id_tipo where u.id_usuario='"+Integer.parseInt(txtUser.getText())+"' and contrasena='"+txtPassword.getText()+"'";
            res = conecta.consultar(sqlConsulta);
            System.out.println(sqlConsulta);
            try {
                if(res.next()){
                    desc=res.getString("descripcion");
                    nom=res.getString("nombre");
                    idU = res.getString("id_usuario");
                    //System.out.println(nom);
                    if(desc.equals("Usuario")){
                        PaginaPrincipalUsuario prinU = new PaginaPrincipalUsuario();
                        prinU.setNombre(nom, idU);
                        prinU.setVisible(true);
                        this.setVisible(false);
                        
                    }else{
                        if(desc.equals("Administrador")){
                        PaginaPrincipalAdministrador prin = new PaginaPrincipalAdministrador();
                        prin.setVisible(true);
                        this.setVisible(false);
                    }
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Usuario no encontrado");
                }
            } catch (Exception e) {
                System.out.println("error al iniciar sesion: "+e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

